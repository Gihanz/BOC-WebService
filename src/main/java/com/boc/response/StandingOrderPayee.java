package com.boc.response;

public class StandingOrderPayee {
	String payee;
	String payeeType;
	String targetType;
	String accountNo;
	String targetDes;
	String feePlan;
	String curCode;
	
	public String getPayee() {
		return payee;
	}
	public void setPayee(String payee) {
		this.payee = payee;
	}
	public String getPayeeType() {
		return payeeType;
	}
	public void setPayeeType(String payeeType) {
		this.payeeType = payeeType;
	}
	public String getTargetType() {
		return targetType;
	}
	public void setTargetType(String targetType) {
		this.targetType = targetType;
	}
	public String getAccountNo() {
		return accountNo;
	}
	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}
	public String getTargetDes() {
		return targetDes;
	}
	public void setTargetDes(String targetDes) {
		this.targetDes = targetDes;
	}
	public String getFeePlan() {
		return feePlan;
	}
	public void setFeePlan(String feePlan) {
		this.feePlan = feePlan;
	}
	public String getCurCode() {
		return curCode;
	}
	public void setCurCode(String curCode) {
		this.curCode = curCode;
	}
	

}
